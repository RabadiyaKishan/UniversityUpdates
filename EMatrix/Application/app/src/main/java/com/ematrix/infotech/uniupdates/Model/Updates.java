package com.ematrix.infotech.uniupdates.Model;

public class Updates {
    String ID;
    String UniversityID;
    String StreamID;
    String English;
    String Gujrati;
    String UniversityLink;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getUniversityID() {
        return UniversityID;
    }

    public void setUniversityID(String universityID) {
        UniversityID = universityID;
    }

    public String getStreamID() {
        return StreamID;
    }

    public void setStreamID(String streamID) {
        StreamID = streamID;
    }

    public String getEnglish() {
        return English;
    }

    public void setEnglish(String english) {
        English = english;
    }

    public String getGujrati() {
        return Gujrati;
    }

    public void setGujrati(String gujrati) {
        Gujrati = gujrati;
    }

    public String getUniversityLink() {
        return UniversityLink;
    }

    public void setUniversityLink(String universityLink) {
        UniversityLink = universityLink;
    }
}
